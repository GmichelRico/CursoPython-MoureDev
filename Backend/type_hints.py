### Type Hints ###

my_stream_variable = "My string variable"
print(my_stream_variable)
print(type(my_stream_variable))


my_stream_variable = 10
print(my_stream_variable)
print(type(my_stream_variable))


my_typed_variable: str = "My typed String variable"
print(my_typed_variable)
print(type(my_typed_variable))


my_typed_variable: int = 10
print(my_typed_variable)
print(type(my_typed_variable))



